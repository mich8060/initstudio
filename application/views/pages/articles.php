				<p>
					It may be odd to come right out and admit I'm a control freak when it comes to building my designs. Honestly there are very few
					individuals I would honestly trust to make my designs come to life. Now I'm not saying that there aren't those people out there,
					I'm just saying that they don't come around all that often. So, in order to survives this world or quickest to the line, and 
					<q>to much control</q> is a bad thing. I've learned to take matters into my own hands.
				</p>
				<p>
					Recently I sat on a panel with three other user experience designers and discussed the concept of designing in the browser.
					Everything I said in the presentation was true, designing the browser is a great way to communicate your concepts and share the
					idea, but there was one dirty little secret I didn't share. I don't want to give my designs to a developer... I want to do it all.
				</p>
				<p>
					Thats right, I said it. Now I love working with others. It's a lot of fun, but nobody cares about those extra two pixels that annoy the
					crap out of me and no one is going to get that transition just perfect because well... they didn't concept it.
